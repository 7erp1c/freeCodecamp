//🥰🥰🥰Selecting from Many Options with Switch Statements
function caseInSwitch(val) {
    let answer = "";
    // Only change code below this line
    switch (val){
        case 1:
            answer = "alpha";
            break;
        case 2:
            answer = "beta";
            break;
        case 3:
            answer = "gamma";
            break;
        case 4:
            answer = "delta";
            break;
    }
    // Only change code above this line
    return answer;
}

caseInSwitch(1);
//😀😀😀Adding a Default Option in Switch Statements
function switchOfStuff(val1) {
    let answer2 = "";
    // Only change code below this line
    switch(val1){
        case "a":
            answer2 = "apple";
            break;
        case "b":
            answer2 = "bird";
            break;
        case "c":
            answer2 = "cat";
            break;
        default:
            answer2 = "stuff";
            break;
    }
    // Only change code above this line
    return answer2;
}
switchOfStuff(1);
